"""Scaffold a new LogicPilot project (v0.7a §4a.1).

Two modes:

- ``--minimal`` (default): generate just ``flow.toml`` + empty ``src/``
  + ``tb/`` + ``.gitignore``. Docs are left for the user to create
  (or for ``--with-templates`` to fill in). Plan-check will produce
  blocked rows until docs exist, with a clear "run /lp-init
  --with-templates" hint.

- ``--with-templates``: additionally scaffold ``docs/spec.md`` /
  ``docs/uarch.md`` / ``docs/plan.md`` with ``<<FILL: …>>`` placeholders
  that plan-check intentionally rejects. The intent is: the user
  immediately sees an actionable plan-check failure pointing at
  specific placeholders, instead of guessing what's missing.

Interactivity: every required field can be supplied via flag. Anything
missing prompts on stdin; ``--non-interactive`` short-circuits that
(useful from CI). Tests inject ``inputs={...}`` to skip ``input()``
entirely.

NEVER overwrites existing files — pre-existing paths are listed in the
``skipped`` array. The user can rerun ``--init`` on a populated repo
without fear of losing work.
"""
from __future__ import annotations

import sys
from pathlib import Path
from typing import Callable


# --- Choices for the four required fields ----------------------------------

HDL_CHOICES: dict[str, str] = {
    "verilog":       "Pure Verilog-2001 (.v files)",
    "systemverilog": "SystemVerilog (.sv files, IEEE 1800-2017)",
    "vhdl":          "VHDL (.vhd / .vhdl files)",
    "mixed":         "Mixed SV + VHDL (mostly chip-level integration)",
}

TARGET_CHOICES: dict[str, str] = {
    "open-fpga":   "open FPGA flow (yosys + nextpnr; iCE40 / ECP5)",
    "vivado":      "Xilinx Vivado (7-series / UltraScale / Zynq)",
    "openlane":    "ASIC via OpenLane / OpenROAD",
    "front-only":  "front-end only (lint / sim / audit; no PnR or synth)",
}

SCOPE_CHOICES: dict[str, str] = {
    "block":   "single IP / peripheral / accelerator / FIFO / bridge",
    "project": "multi-partition design (SoC, large algorithm, …)",
}


# --- Template strings ------------------------------------------------------

_FLOW_TOML_MINIMAL = """\
# Generated by /lp-init at {date}.
# See shared/flow/flow.toml.example for every option.

[project]
top = "{top}"
src = ["src/**/*.{hdl_ext}"]
tb  = ["tb/**/*.{hdl_ext}"]

[toolchain]
preset = "{preset}"
"""

_GITIGNORE = """\
# LogicPilot driver artifacts
build/
obj_dir/
*.vcd
*.fst
*.log
__pycache__/
"""

_SPEC_TEMPLATE = """\
# Spec: {top}

**Scope**: {scope}
**Generated by `/lp-init --with-templates` on {date}.**
Replace every `<<FILL: ...>>` placeholder before running `/lp-front`;
plan-check will fail otherwise (intentionally — placeholders are a
"you haven't decided yet" signal).

## 1. Purpose

<<FILL: one paragraph describing what this block does, what comes in,
what goes out, and the one external contract it must honor>>

## 2. Inputs and outputs

- **Clocks**: <<FILL: list every clock; mark async pairs>>
- **Resets**: <<FILL: per clock; sync vs async deassert>>
- **Bus interface**: <<FILL: AXI4-Lite / APB / custom; with handshake or registered>>
- **Side-band signals**: <<FILL: IRQ / status flags / debug>>

## 3. Decisions made (and why)

- <<FILL: each row is a decision the agent extracted from Q&A with you;
  if a row is missing, plan-check is right — re-run the brainstorm round>>

## 4. Explicitly out of scope

- <<FILL: each row is something a reader might assume is in this block
  but isn't; the agent will produce stub modules for these>>
"""

_UARCH_TEMPLATE = """\
# Microarchitecture: {top}

**Generated by `/lp-init --with-templates` on {date}.**

## 1. Block diagram (text)

<<FILL: ASCII block diagram showing internal partitioning; one box per
RTL module you intend to write>>

## 2. File / module list with test allocation

| File | Test approach | Notes |
|------|---------------|-------|
| <<FILL: src/foo.sv>> | <<FILL: reference-model / property-invariant / integration>> | <<FILL: rationale>> |

Every row's "Test approach" cell MUST be one of: `reference-model`,
`property-invariant`, `integration`. Plan-check does not enforce this
yet, but `hardware-design-discipline` skill does.

## 3. State machines

<<FILL: per FSM, list states + transitions + idle/error recovery>>

## 4. Pipeline / dataflow

<<FILL: cycle-level dataflow if applicable>>
"""

_PLAN_TEMPLATE = """\
# Plan: {top}

**Generated by `/lp-init --with-templates` on {date}.**

The plan is a resumable execution log. Use markdown checkboxes
(`- [ ] task` / `- [x] done`) so a dropped session can pick up by
reading unchecked rows. Plan-check requires at least one `- [ ]`
checkbox in this file.

## Phase 1: scaffolding

- [ ] <<FILL: src/{top}_top.{hdl_ext} ← refers to uarch.md row 1>>
- [ ] <<FILL: tb/{top}_tb.{hdl_ext} ← refers to uarch.md test allocation>>

## Phase 2: verification

- [ ] <<FILL: smoke test passes with returncode 0>>
- [ ] <<FILL: every uarch decision has at least one assertion or test>>

## Notes

<<FILL: anything you want to remember for next session>>
"""


def _next_step_banner(created: list[str], with_templates: bool) -> str:
    """Tail message printed by run_init telling the user what to do next."""
    lines = ["Next step:"]
    if with_templates:
        lines.append("  1. Open docs/spec.md and replace every <<FILL: ...>> placeholder.")
        lines.append("  2. Same for docs/uarch.md and docs/plan.md.")
        lines.append("  3. Run /lp-front — plan-check will fail until placeholders are removed (by design).")
    else:
        lines.append("  1. Add RTL to src/ and a testbench to tb/.")
        lines.append("  2. Run /lp-doctor to verify your setup.")
        lines.append("  3. Run /lp-init --with-templates if you want spec / uarch / plan scaffolds.")
    return "\n".join(lines)


# --- choice resolution -----------------------------------------------------

def _ask(prompt: str, choices: dict[str, str], default: str | None,
         input_fn: Callable[[str], str]) -> str:
    """Prompt the user with a choice list. Returns the chosen key.
    Caller must validate the key is in `choices`."""
    lines = [prompt]
    for key, desc in choices.items():
        marker = " (default)" if key == default else ""
        lines.append(f"  {key}: {desc}{marker}")
    while True:
        raw = input_fn("\n".join(lines) + "\n> ").strip().lower()
        if not raw and default is not None:
            return default
        if raw in choices:
            return raw
        lines = [f"  ↳ '{raw}' is not a valid choice. Pick one of: {', '.join(choices)}."]


def _resolve_choices(
    *,
    hdl: str | None,
    target: str | None,
    scope: str | None,
    top: str | None,
    interactive: bool,
    inputs: dict[str, str] | None,
    input_fn: Callable[[str], str],
) -> dict[str, str]:
    """Return a {hdl, target, scope, top} dict, prompting only for missing
    values. ``inputs`` is a test-only shortcut: when present, it supplies
    the same values input_fn would have returned (no stdin reads)."""
    resolved: dict[str, str | None] = {
        "hdl": hdl, "target": target, "scope": scope, "top": top,
    }
    if inputs:
        for key, val in inputs.items():
            if resolved.get(key) is None:
                resolved[key] = val

    def _need(key: str, choices: dict[str, str], default: str) -> str:
        if resolved[key] is not None:
            val = str(resolved[key]).lower()
            if val in choices:
                return val
            raise ValueError(
                f"--{key.replace('_', '-')} must be one of "
                f"{', '.join(choices)} (got {resolved[key]!r})"
            )
        if not interactive:
            return default
        return _ask(f"Choose {key}:", choices, default, input_fn)

    hdl_v = _need("hdl", HDL_CHOICES, "systemverilog")
    target_v = _need("target", TARGET_CHOICES, "open-fpga")
    scope_v = _need("scope", SCOPE_CHOICES, "block")
    top_v = resolved["top"] or "top"
    if not top_v.isidentifier():
        raise ValueError(
            f"--top '{top_v}' is not a legal HDL identifier "
            "(use letters, digits, underscore; must start with letter/underscore)"
        )

    return {"hdl": hdl_v, "target": target_v, "scope": scope_v, "top": top_v}


def _hdl_ext(hdl: str) -> str:
    return {"verilog": "v", "systemverilog": "sv",
            "vhdl": "vhd", "mixed": "sv"}[hdl]


def _preset_for(target: str) -> str:
    return {"open-fpga": "yosys-nextpnr",
            "vivado": "vivado",
            "openlane": "asic-openlane",
            "front-only": "yosys-nextpnr"}[target]  # closest preset


# --- file writers ----------------------------------------------------------

def _write(path: Path, content: str, created: list[str], skipped: list[str]) -> None:
    """Write content to path unless it already exists. Records the action."""
    rel = str(path)
    if path.exists():
        skipped.append(rel)
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content)
    created.append(rel)


def run_init(
    target_dir: Path,
    *,
    with_templates: bool = False,
    hdl: str | None = None,
    target: str | None = None,
    scope: str | None = None,
    top: str | None = None,
    interactive: bool = True,
    inputs: dict[str, str] | None = None,
    input_fn: Callable[[str], str] = input,
    now: str | None = None,
) -> dict:
    """Scaffold a LogicPilot project. Returns
    {stage, status, mode, choices, created, skipped, next_step}."""
    from datetime import datetime, timezone

    choices = _resolve_choices(
        hdl=hdl, target=target, scope=scope, top=top,
        interactive=interactive, inputs=inputs, input_fn=input_fn,
    )

    date = now or datetime.now(timezone.utc).strftime("%Y-%m-%d")
    hdl_ext = _hdl_ext(choices["hdl"])
    preset = _preset_for(choices["target"])

    target_dir = target_dir.resolve()
    target_dir.mkdir(parents=True, exist_ok=True)
    created: list[str] = []
    skipped: list[str] = []

    flow_toml = _FLOW_TOML_MINIMAL.format(
        date=date, top=choices["top"], hdl_ext=hdl_ext, preset=preset,
    )
    _write(target_dir / "flow.toml", flow_toml, created, skipped)
    _write(target_dir / ".gitignore", _GITIGNORE, created, skipped)
    _write(target_dir / "src" / ".gitkeep", "", created, skipped)
    _write(target_dir / "tb" / ".gitkeep", "", created, skipped)

    if with_templates:
        ctx = {"top": choices["top"], "scope": choices["scope"],
               "date": date, "hdl_ext": hdl_ext}
        _write(target_dir / "docs" / "spec.md",
               _SPEC_TEMPLATE.format(**ctx), created, skipped)
        _write(target_dir / "docs" / "uarch.md",
               _UARCH_TEMPLATE.format(**ctx), created, skipped)
        _write(target_dir / "docs" / "plan.md",
               _PLAN_TEMPLATE.format(**ctx), created, skipped)

    return {
        "stage": "init",
        "status": "pass" if created else "blocked",
        "mode": "with-templates" if with_templates else "minimal",
        "choices": choices,
        "created": created,
        "skipped": skipped,
        "next_step": _next_step_banner(created, with_templates),
    }
